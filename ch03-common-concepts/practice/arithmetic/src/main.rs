fn main() {
    // addition
    let sum = 5 + 10;
    println!("{}", sum); // 15
    
    // subtraction
    let difference = 95.5 - 4.3;
    println!("{}", difference); // 91.2
    
    // multiplication
    let product = 4 * 30;
    println!("{}", product); // 120
    
    // division
    let quotient = 56.7 / 32.2;
    println!("{}", quotient); // 1.7608695652173911
    let truncated = -5 / 3;
    println!("{}", truncated); // -1
    
    // modulus
    let remainder = 43 % 5;
    println!("{}", remainder); // 3
}
